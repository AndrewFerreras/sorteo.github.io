function ObtenerRamdonEliminar(list) {
  if (list.length === 0) {
      return null; // Retorna null si la lista está vacía
  }

  const randomIndex = Math.floor(Math.random() * list.length);
  const randomValue = list[randomIndex];

  // Elimina el valor seleccionado de la lista
  list.splice(randomIndex, 1);

  return randomValue;
}

